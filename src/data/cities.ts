export function getCitiesByProvince(province: string): CityInfo[] {
  return CITIES.filter(c => c.province === province);
}

export function searchCities(query: string): CityInfo[] {
  const q = query.toLowerCase().trim();
  if (!q) return [];
  return CITIES.filter(c =>
    c.name.includes(q) || c.province.includes(q)
  );
}

export function getCityByCode(code: string): CityInfo | undefined {
  return CITIES.find(c => c.code === code);
}
